# declare variable with data type number.
a = 10
print(a)

b = 30
print(b)
print(a * b)

# declare variable with data type string.
string = "hello world"
print(string)

# declare variable by starting underscore(_) character.
_underscore = "_hello"
print(_underscore)

# assign multiple variables in single line
x, y, z = 20, 35, 40
print(x, y, z)

# destructuring of array or unpacking of array
num = [50, 40, 30]
a, b, c = num
print(c, b, a)

# conditions for creating variables.
# A variable name must start with a letter or the underscore character
# A variable name cannot start with a number
# A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
# Variable names are case-sensitive (age, Age and AGE are three different variables)
# A variable name cannot be any of the Python keywords.
